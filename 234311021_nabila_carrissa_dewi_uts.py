    plt.tight_layout()
    out_path = os.path.join(CONFIG["OUTPUT_DIR"], "prediksi_gambar_baru.png")
    plt.savefig(out_path, dpi=150)
    plt.show()
    print(f"  Prediksi disimpan: {out_path}")

    return {"CNN": pred_cnn, "SVM": pred_svm,
            "Decision Tree": pred_dt, "Random Forest": pred_rf}


