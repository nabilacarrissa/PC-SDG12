# Catatan metodologi:
# Haqqi et al. (2024) menerapkan cropping, Gaussian filter, dan augmentasi
# untuk meningkatkan kualitas data.
# Lau et al. (2025) menggunakan normalisasi dan ekstraksi HOG.
# Penelitian ini menggabungkan kedua pendekatan tersebut.

def load_and_preprocess_dataset(dataset_dir, img_size, img_size_hog):
    """
    Memuat seluruh dataset gambar, melakukan preprocessing, dan
    mengembalikan array gambar beserta label-nya.

    Args:
        dataset_dir  : path root folder dataset
        img_size     : tuple (h, w) untuk CNN
        img_size_hog : tuple (h, w) untuk HOG / ML klasik

    Returns:
        images_cnn  : array (N, H, W, 3) ternormalisasi untuk CNN
        images_hog  : array (N, H, W, 3) untuk ekstraksi HOG
        labels      : array string nama kelas
        label_enc   : LabelEncoder yang sudah di-fit
    """
    images_cnn  = []
    images_hog  = []
    labels      = []
    dataset_dir = Path(dataset_dir)

    print("\n[LOAD] Memuat dataset ...")
    for kelas in KELAS:
        kelas_dir = dataset_dir / kelas
        if not kelas_dir.exists():
            print(f"  [PERINGATAN] Folder tidak ditemukan: {kelas_dir}")
            continue
        gambar_files = list(kelas_dir.glob("*.jpg")) + \
                       list(kelas_dir.glob("*.jpeg")) + \
                       list(kelas_dir.glob("*.png"))
        print(f"  Kelas '{kelas}': {len(gambar_files)} gambar")

        for filepath in gambar_files:
            img_bgr = cv2.imread(str(filepath))
            if img_bgr is None:
                continue  # lewati gambar rusak

            img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)

            # --- Gaussian filter untuk noise reduction (Haqqi et al., 2024) ---
            img_filtered = cv2.GaussianBlur(img_rgb, (3, 3), 0)

            # --- Resize untuk CNN ---
            img_cnn = cv2.resize(img_filtered, (img_size[1], img_size[0]))
            img_cnn = img_cnn.astype(np.float32) / 255.0   # normalisasi [0,1]

            # --- Resize untuk HOG (ukuran lebih kecil) ---
            img_hog = cv2.resize(img_filtered, (img_size_hog[1], img_size_hog[0]))

            images_cnn.append(img_cnn)
            images_hog.append(img_hog)
            labels.append(kelas)

    images_cnn = np.array(images_cnn, dtype=np.float32)
    images_hog = np.array(images_hog, dtype=np.uint8)
    labels     = np.array(labels)

    # Encode label ke integer
    label_enc = LabelEncoder()
    label_enc.fit(KELAS)

    print(f"\n  Total gambar dimuat : {len(labels)}")
    print(f"  Distribusi kelas    : {dict(Counter(labels))}")
    return images_cnn, images_hog, labels, label_enc


