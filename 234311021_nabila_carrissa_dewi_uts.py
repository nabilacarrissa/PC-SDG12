# CNN : fitur diekstraksi secara otomatis oleh lapisan konvolusi
# ML  : fitur diekstraksi menggunakan HOG (Lau et al., 2025)

def ekstraksi_hog(images_hog, orientations=9, pixels_per_cell=(8, 8),
                  cells_per_block=(2, 2)):
    """
    Mengekstraksi fitur HOG (Histogram of Oriented Gradients) dari
    setiap gambar. HOG menangkap informasi bentuk dan tepi objek
    (Lau et al., 2025; Ochango et al., 2022).

    Args:
        images_hog : array gambar (N, H, W, 3) uint8
        orientations, pixels_per_cell, cells_per_block : parameter HOG

    Returns:
        features : array (N, d) fitur HOG
    """
    print("\n[FITUR] Mengekstraksi fitur HOG ...")
    features = []
    for img in images_hog:
        # Konversi ke grayscale untuk HOG
        img_gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)
        feat = hog(
            img_gray,
            orientations=orientations,
            pixels_per_cell=pixels_per_cell,
            cells_per_block=cells_per_block,
            visualize=False,
            feature_vector=True)
        features.append(feat)
    features = np.array(features, dtype=np.float32)
    print(f"  Dimensi fitur HOG: {features.shape}")
    return features


