# Rasio 70:20:10 sesuai Haqqi et al. (2024)

def bagi_data(images_cnn, features_hog, labels, label_enc, config):
    """
    Membagi data menjadi tiga subset: train, validasi, dan test.
    Stratified split digunakan agar distribusi kelas seimbang.

    Rasio: 70% train · 20% validasi · 10% test
    (Haqqi et al., 2024)
    """
    labels_int = label_enc.transform(labels)
    rs = config["RANDOM_STATE"]

    # Split pertama: pisahkan test (10%)
    X_cnn_tv, X_cnn_test, X_hog_tv, X_hog_test, y_tv, y_test = \
        train_test_split(images_cnn, features_hog, labels_int,
                         test_size=config["TEST_RATIO"],
                         stratify=labels_int,
                         random_state=rs)

    # Split kedua: pisahkan validasi dari sisa (20/90 ≈ 22.2%)
    val_ratio_adj = config["VAL_RATIO"] / (1.0 - config["TEST_RATIO"])
    X_cnn_train, X_cnn_val, X_hog_train, X_hog_val, y_train, y_val = \
        train_test_split(X_cnn_tv, X_hog_tv, y_tv,
                         test_size=val_ratio_adj,
                         stratify=y_tv,
                         random_state=rs)

    # Standarisasi fitur HOG (penting untuk SVM)
    scaler = StandardScaler()
    X_hog_train = scaler.fit_transform(X_hog_train)
    X_hog_val   = scaler.transform(X_hog_val)
    X_hog_test  = scaler.transform(X_hog_test)

    print("\n[SPLIT] Pembagian data:")
    print(f"  Train      : {len(y_train):>5} sampel ({len(y_train)/len(labels)*100:.1f}%)")
    print(f"  Validasi   : {len(y_val):>5} sampel ({len(y_val)/len(labels)*100:.1f}%)")
    print(f"  Test       : {len(y_test):>5} sampel ({len(y_test)/len(labels)*100:.1f}%)")

    return (X_cnn_train, X_cnn_val, X_cnn_test,
            X_hog_train, X_hog_val, X_hog_test,
            y_train, y_val, y_test, scaler)


