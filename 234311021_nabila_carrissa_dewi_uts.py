    print(f"\n[PREDIKSI] Gambar: {path_gambar}")

    img_bgr = cv2.imread(str(path_gambar))
    if img_bgr is None:
        print("  ERROR: Gambar tidak dapat dibaca.")
        return

    img_rgb      = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
    img_filtered = cv2.GaussianBlur(img_rgb, (3, 3), 0)

    # Persiapan untuk CNN
    img_cnn = cv2.resize(img_filtered, (config["IMG_SIZE"][1], config["IMG_SIZE"][0]))
    img_cnn = img_cnn.astype(np.float32) / 255.0
    img_cnn_batch = np.expand_dims(img_cnn, axis=0)

    # Persiapan untuk HOG
    img_hog_raw = cv2.resize(img_filtered,
                              (config["IMG_SIZE_HOG"][1], config["IMG_SIZE_HOG"][0]))
    img_gray    = cv2.cvtColor(img_hog_raw, cv2.COLOR_RGB2GRAY)
    feat        = hog(img_gray, orientations=9,
                      pixels_per_cell=(8, 8), cells_per_block=(2, 2),
                      visualize=False, feature_vector=True)
    feat_scaled = scaler.transform([feat])

    # Prediksi
    pred_cnn = label_enc.inverse_transform([np.argmax(model_cnn.predict(img_cnn_batch))])[0]
    pred_svm = label_enc.inverse_transform(model_svm.predict(feat_scaled))[0]
    pred_dt  = label_enc.inverse_transform(model_dt.predict(feat_scaled))[0]
    pred_rf  = label_enc.inverse_transform(model_rf.predict(feat_scaled))[0]

    # Tampilkan gambar + prediksi
    fig, axes = plt.subplots(1, 2, figsize=(10, 5))
    axes[0].imshow(img_rgb)
    axes[0].set_title("Gambar Input", fontsize=12)
    axes[0].axis('off')

    info = (f"Prediksi Model:\n"
            f"  CNN          : {pred_cnn}\n"
            f"  SVM (RBF)    : {pred_svm}\n"
            f"  Decision Tree: {pred_dt}\n"
            f"  Random Forest: {pred_rf}")
