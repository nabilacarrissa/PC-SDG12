def evaluasi_model(nama, model, X_test, y_test, label_enc,
                   is_cnn=False, output_dir="./output_model"):
    """
    Menghitung akurasi, menampilkan classification report, dan
    menyimpan confusion matrix untuk satu model.

    Returns:
        dict berisi akurasi dan metrik lainnya
    """
    print(f"\n--- Evaluasi: {nama} ---")

    if is_cnn:
        y_pred_prob = model.predict(X_test)
        y_pred      = np.argmax(y_pred_prob, axis=1)
    else:
        y_pred = model.predict(X_test)

    akurasi = accuracy_score(y_test, y_pred)
    report  = classification_report(y_test, y_pred,
                                     target_names=label_enc.classes_,
                                     output_dict=True)
    cm      = confusion_matrix(y_test, y_pred)

    print(f"  Akurasi : {akurasi*100:.2f}%")
    print(classification_report(y_test, y_pred,
                                 target_names=label_enc.classes_))

    # Simpan confusion matrix sebagai gambar
    fig, ax = plt.subplots(figsize=(8, 6))
    disp = ConfusionMatrixDisplay(confusion_matrix=cm,
                                   display_labels=label_enc.classes_)
    disp.plot(ax=ax, colorbar=True, cmap='Blues')
    ax.set_title(f"Confusion Matrix — {nama}", fontsize=13, pad=12)
    plt.tight_layout()
    cm_path = os.path.join(output_dir, f"cm_{nama.replace(' ', '_')}.png")
    plt.savefig(cm_path, dpi=150)
    plt.close()
    print(f"  Confusion matrix disimpan: {cm_path}")

    return {
        "nama"     : nama,
        "akurasi"  : akurasi,
        "presisi"  : report["weighted avg"]["precision"],
        "recall"   : report["weighted avg"]["recall"],
        "f1_score" : report["weighted avg"]["f1-score"],
        "cm"       : cm,
    }


