def preprocess_gambar(path_gambar):
    """Baca gambar dan siapkan untuk CNN dan HOG."""
    img_bgr     = cv2.imread(path_gambar)
    img_rgb     = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
    img_filter  = cv2.GaussianBlur(img_rgb, (3, 3), 0)

    # Untuk CNN — 224x224, normalisasi 0–1
    img_cnn = cv2.resize(img_filter, (IMG_SIZE_CNN[1], IMG_SIZE_CNN[0]))
    img_cnn = img_cnn.astype(np.float32) / 255.0
    img_cnn = np.expand_dims(img_cnn, axis=0)

    # Untuk HOG — 64x64, ekstraksi fitur
    img_hog  = cv2.resize(img_filter, (IMG_SIZE_HOG[1], IMG_SIZE_HOG[0]))
    img_gray = cv2.cvtColor(img_hog, cv2.COLOR_RGB2GRAY)
    fitur    = hog(img_gray, orientations=9,
                   pixels_per_cell=(8, 8), cells_per_block=(2, 2),
                   visualize=False, feature_vector=True)
    fitur    = scaler.transform([fitur]) if scaler else fitur.reshape(1, -1)

    return img_rgb, img_cnn, fitur


def prediksi_semua(path_gambar):
    """Jalankan prediksi dari keempat model dan kembalikan hasilnya."""
    img_asli, img_cnn, fitur = preprocess_gambar(path_gambar)
    hasil = {}

    # SVM
    if model_svm:
        idx = model_svm.predict(fitur)[0]
        prob = model_svm.predict_proba(fitur)[0]
        hasil['SVM RBF'] = {'kelas': KELAS[idx], 'prob': prob, 'confidence': prob[idx]}

    # Random Forest
    if model_rf:
        idx = model_rf.predict(fitur)[0]
        prob = model_rf.predict_proba(fitur)[0]
        hasil['Random Forest'] = {'kelas': KELAS[idx], 'prob': prob, 'confidence': prob[idx]}

    # Decision Tree
    if model_dt:
        idx = model_dt.predict(fitur)[0]
        prob = model_dt.predict_proba(fitur)[0]
        hasil['Decision Tree'] = {'kelas': KELAS[idx], 'prob': prob, 'confidence': prob[idx]}

    # CNN
    if model_cnn:
        prob = model_cnn.predict(img_cnn, verbose=0)[0]
        idx  = np.argmax(prob)
        hasil['CNN'] = {'kelas': KELAS[idx], 'prob': prob, 'confidence': prob[idx]}

    return img_asli, hasil


