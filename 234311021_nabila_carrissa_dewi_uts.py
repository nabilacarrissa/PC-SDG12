# ---------------------------------------------------------------------------
# F1. Model CNN
# ---------------------------------------------------------------------------
# Arsitektur berbasis Haqqi et al. (2024): konvolusi → aktivasi → pooling →
# fully connected → softmax. Dimodifikasi dengan tambahan Batch Normalization
# dan Dropout untuk regularisasi.

def bangun_model_cnn(num_kelas, img_size, dropout_rate, learning_rate):
    """
    Membangun arsitektur CNN untuk klasifikasi gambar sampah.

    Arsitektur (Haqqi et al., 2024):
      - Conv2D (32 filter, 3x3) → BatchNorm → ReLU → MaxPool
      - Conv2D (64 filter, 3x3) → BatchNorm → ReLU → MaxPool
      - Conv2D (128 filter, 3x3) → BatchNorm → ReLU → MaxPool
      - GlobalAveragePooling
      - Dense (256) → Dropout → Dense (num_kelas) → Softmax
    """
    model = keras.Sequential([
        # --- Blok 1 ---
        layers.Conv2D(32, (3, 3), padding='same',
                      input_shape=(*img_size, 3)),
        layers.BatchNormalization(),
        layers.Activation('relu'),
        layers.MaxPooling2D(2, 2),

        # --- Blok 2 ---
        layers.Conv2D(64, (3, 3), padding='same'),
        layers.BatchNormalization(),
        layers.Activation('relu'),
        layers.MaxPooling2D(2, 2),

        # --- Blok 3 ---
        layers.Conv2D(128, (3, 3), padding='same'),
        layers.BatchNormalization(),
        layers.Activation('relu'),
        layers.MaxPooling2D(2, 2),

        # --- Blok 4 ---
        layers.Conv2D(256, (3, 3), padding='same'),
        layers.BatchNormalization(),
        layers.Activation('relu'),
        layers.MaxPooling2D(2, 2),

        # --- Klasifikasi ---
        layers.GlobalAveragePooling2D(),
        layers.Dense(256, activation='relu'),
        layers.Dropout(dropout_rate),
        layers.Dense(num_kelas, activation='softmax'),
    ], name="CNN_Klasifikasi_Sampah")

    model.compile(
        optimizer=keras.optimizers.Adam(learning_rate=learning_rate),
        loss='sparse_categorical_crossentropy',
        metrics=['accuracy']
    )
    return model


def latih_cnn(X_train, y_train, X_val, y_val, config):
    """
    Melatih model CNN dengan callbacks EarlyStopping dan ReduceLROnPlateau.
    Data augmentasi diterapkan pada data pelatihan.
    """
    print("\n" + "="*50)
    print("[CNN] Memulai pelatihan ...")
    print("="*50)

    model = bangun_model_cnn(
        NUM_KELAS,
        config["IMG_SIZE"],
        config["DROPOUT_RATE"],
        config["LEARNING_RATE"]
    )
    model.summary()

    # Augmentasi data (Haqqi et al., 2024): horizontal flip, rotation, zoom
    datagen = ImageDataGenerator(
        rotation_range=20,
        width_shift_range=0.15,
        height_shift_range=0.15,
        horizontal_flip=True,
        zoom_range=0.15,
        brightness_range=[0.8, 1.2],
    )

    cb_list = [
        callbacks.EarlyStopping(monitor='val_accuracy', patience=10,
                                restore_best_weights=True, verbose=1),
        callbacks.ReduceLROnPlateau(monitor='val_loss', factor=0.5,
                                    patience=5, verbose=1, min_lr=1e-6),
        callbacks.ModelCheckpoint(
            filepath=os.path.join(config["OUTPUT_DIR"], "best_cnn.h5"),
            monitor='val_accuracy', save_best_only=True, verbose=0
        ),
    ]

    history = model.fit(
        datagen.flow(X_train, y_train, batch_size=config["BATCH_SIZE"]),
        steps_per_epoch=len(X_train) // config["BATCH_SIZE"],
        validation_data=(X_val, y_val),
        epochs=config["EPOCHS"],
        callbacks=cb_list,
        verbose=1
    )

    return model, history


